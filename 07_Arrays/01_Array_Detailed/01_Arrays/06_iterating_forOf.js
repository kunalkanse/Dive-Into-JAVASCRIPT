
const num = [10, 20, 30, 40, 50, 60]

//////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////
// forof Loop

for (const iterator of num) {
    console.log(iterator);
}

/*
10
20
30
40
50
60
*/

console.log();

//////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////

for (const items of num.entries()) {
    console.log(`${items[0] + 1} : ${items[1]}`);
}

/*
1 : 10
2 : 20
3 : 30
4 : 40
5 : 50
6 : 60
*/
console.log();

///////////////////////////////////////////////////////////

const newNum = [...num.entries()]
console.log(newNum);
// [ [ 0, 10 ], [ 1, 20 ], [ 2, 30 ], [ 3, 40 ], [ 4, 50 ], [ 5, 60 ] ] 


/////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////
// destructuring forof Loop
for (const [i, elem] of num.entries()) {
    console.log(`${i + 1} : ${elem}`);
}

/*
1 : 10
2 : 20
3 : 30
4 : 40
5 : 50
6 : 60
*/
